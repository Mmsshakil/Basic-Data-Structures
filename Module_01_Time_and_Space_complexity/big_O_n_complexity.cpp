#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;    // complexity O(1)
    cin >> n; // complexity O(1)

    for (int i = 0; i < n; i++) // complexity O(n)
    {
        cout << i << endl;
    }

    return 0; // complexity O(1)
}

// in this code wrost case is O(n)
// so complexity is O(n)